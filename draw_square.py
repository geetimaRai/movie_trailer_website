import turtle

def draw_square():
    myTurtle = turtle.Turtle()

    window = turtle.Screen()
    window.bgcolor("red")

    myTurtle.forward(100)
    myTurtle.right(90)
    myTurtle.forward(100)
    myTurtle.right(90)
    myTurtle.forward(100)
    myTurtle.right(90)
    myTurtle.forward(100)
    myTurtle.right(90)

